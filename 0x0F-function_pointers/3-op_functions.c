#include "3-calc.h"

/**
* op_add - adds two numbers.
*
* @a: integer argument.
* @b: integer argument.
*
* Return: the sum of a and b.
*
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtracts two number.
*
* @a: integer argument.
* @b: integer argument.
*
* Return: the subtract of a and b.
*
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplies two numbers.
*
* @a: integer argument.
* @b: integer argument.
*
* Return: the multiplication of a and b.
*
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - devides two numbers.
*
* @a: integer argument.
* @b: integer argument.
*
* Return: the division of two numbers.
*
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - calculate modules of two numbers.
*
* @a: integer argument.
* @b: integer argument.
*
* Return: modules of a and b.
*
*/

int op_mod(int a, int b)
{
	return (a % b);
}
